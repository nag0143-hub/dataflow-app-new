function generateSparkScript(pipeline, sourceConn, targetConn, advancedFeatures) {
  const pipelineName = (pipeline.name || "pipeline").replace(/[^a-z0-9_]/gi, "_").toLowerCase();
  const srcPlatform = sourceConn?.platform || "oracle";
  const tgtPlatform = targetConn?.platform || "local_fs";

  const hasColumnMapping = advancedFeatures?.column_mapping !== false;
  const hasCleansing = advancedFeatures?.data_cleansing !== false;
  const hasDQ = advancedFeatures?.data_quality !== false;
  const hasMasking = advancedFeatures?.security !== false;

  const lines = [];

  lines.push(`#!/usr/bin/env python3`);
  lines.push(`"""PySpark extraction and transformation script for pipeline: ${pipelineName}`);
  lines.push(``);
  lines.push(`Auto-generated by DataFlow. Handles:`);
  lines.push(`  - RDBMS extraction via JDBC (Oracle, SQL Server, PostgreSQL, MySQL, MongoDB)`);
  lines.push(`  - Target writes (local parquet, ADLS2 abfss://, S3 s3a://)`);
  if (hasColumnMapping) lines.push(`  - Column mapping/renaming via mapping JSON`);
  if (hasCleansing) lines.push(`  - Data cleansing transforms`);
  if (hasDQ) lines.push(`  - Data quality validation`);
  if (hasMasking) lines.push(`  - Security/masking transforms`);
  lines.push(`"""`);
  lines.push(``);
  lines.push(`import argparse`);
  lines.push(`import json`);
  lines.push(`import logging`);
  lines.push(`import os`);
  lines.push(`import sys`);
  lines.push(`import hashlib`);
  lines.push(`from datetime import datetime`);
  lines.push(``);
  lines.push(`from pyspark.sql import SparkSession`);
  lines.push(`from pyspark.sql import functions as F`);
  lines.push(`from pyspark.sql.types import StringType`);
  lines.push(``);
  lines.push(`logging.basicConfig(`);
  lines.push(`    level=logging.INFO,`);
  lines.push(`    format="%(asctime)s [%(levelname)s] %(name)s - %(message)s",`);
  lines.push(`)`);
  lines.push(`logger = logging.getLogger("dataflow.spark.${pipelineName}")`);
  lines.push(``);

  lines.push(generateJdbcUrlBuilder());
  lines.push(``);
  lines.push(generateGetConnectionConfig());
  lines.push(``);
  lines.push(generateExtractFunction());
  lines.push(``);
  lines.push(generateWriteFunction());
  lines.push(``);

  if (hasColumnMapping) {
    lines.push(generateColumnMappingFunction());
    lines.push(``);
  }

  if (hasCleansing) {
    lines.push(generateCleansingFunction());
    lines.push(``);
  }

  if (hasDQ) {
    lines.push(generateDQFunction());
    lines.push(``);
  }

  if (hasMasking) {
    lines.push(generateMaskingFunction());
    lines.push(``);
  }

  lines.push(generateMainFunction(hasColumnMapping, hasCleansing, hasDQ, hasMasking));
  lines.push(``);
  lines.push(`if __name__ == "__main__":`);
  lines.push(`    main()`);

  return lines.join("\n");
}

function generateJdbcUrlBuilder() {
  return `
def build_jdbc_url(platform, host, port, database, extra_params=None):
    """Build JDBC connection URL based on source platform."""
    urls = {
        "oracle": f"jdbc:oracle:thin:@{host}:{port or 1521}:{database}",
        "sql_server": f"jdbc:sqlserver://{host}:{port or 1433};databaseName={database}",
        "postgresql": f"jdbc:postgresql://{host}:{port or 5432}/{database}",
        "mysql": f"jdbc:mysql://{host}:{port or 3306}/{database}",
    }
    url = urls.get(platform)
    if url is None:
        raise ValueError(f"Unsupported JDBC platform: {platform}")
    if extra_params:
        separator = "&" if "?" in url else (";" if platform == "sql_server" else "?")
        url += separator + extra_params
    return url


def get_jdbc_driver(platform):
    """Return the JDBC driver class for a given platform."""
    drivers = {
        "oracle": "oracle.jdbc.OracleDriver",
        "sql_server": "com.microsoft.sqlserver.jdbc.SQLServerDriver",
        "postgresql": "org.postgresql.Driver",
        "mysql": "com.mysql.cj.jdbc.Driver",
    }
    driver = drivers.get(platform)
    if driver is None:
        raise ValueError(f"No JDBC driver configured for platform: {platform}")
    return driver`.trim();
}

function generateGetConnectionConfig() {
  return `
def get_connection_config(prefix):
    """Retrieve connection configuration from environment variables or Airflow Variables.

    Looks for variables with the given prefix, e.g.:
      DATAFLOW_SOURCE_HOST, DATAFLOW_SOURCE_PORT, etc.
    Falls back to Airflow Variable if available.
    """
    config = {}
    keys = ["HOST", "PORT", "DATABASE", "USERNAME", "PASSWORD", "EXTRA_PARAMS",
            "CONTAINER", "ACCOUNT_NAME", "ACCOUNT_KEY", "BUCKET", "REGION",
            "ACCESS_KEY", "SECRET_KEY", "CONNECTION_STRING"]
    for key in keys:
        env_key = f"DATAFLOW_{prefix}_{key}"
        val = os.environ.get(env_key)
        if val is None:
            try:
                from airflow.models import Variable
                val = Variable.get(env_key, default_var=None)
            except (ImportError, Exception):
                pass
        if val is not None:
            config[key.lower()] = val
    return config`.trim();
}

function generateExtractFunction() {
  return `
def extract_from_source(spark, platform, schema, table, filter_query=None,
                        incremental_column=None, conn_config=None):
    """Extract data from an RDBMS source via JDBC or from MongoDB."""
    conn_config = conn_config or {}
    logger.info(f"Extracting {schema}.{table} from {platform}")

    if platform == "mongodb":
        mongo_uri = conn_config.get("connection_string") or (
            f"mongodb://{conn_config.get('username', 'root')}:{conn_config.get('password', '')}@"
            f"{conn_config.get('host', 'localhost')}:{conn_config.get('port', '27017')}/{conn_config.get('database', schema)}"
        )
        df = (spark.read
              .format("mongodb")
              .option("uri", mongo_uri)
              .option("database", schema)
              .option("collection", table)
              .load())
        if filter_query:
            df = df.filter(filter_query)
        logger.info(f"Extracted {df.count()} records from MongoDB {schema}.{table}")
        return df

    jdbc_url = build_jdbc_url(
        platform,
        conn_config.get("host", "localhost"),
        conn_config.get("port"),
        conn_config.get("database", schema),
        conn_config.get("extra_params"),
    )
    driver = get_jdbc_driver(platform)

    full_table = f"{schema}.{table}"
    if filter_query:
        dbtable = f"(SELECT * FROM {full_table} WHERE {filter_query}) sub"
    else:
        dbtable = full_table

    reader = (spark.read
              .format("jdbc")
              .option("url", jdbc_url)
              .option("dbtable", dbtable)
              .option("driver", driver)
              .option("user", conn_config.get("username", ""))
              .option("password", conn_config.get("password", "")))

    if incremental_column:
        reader = (reader
                  .option("partitionColumn", incremental_column)
                  .option("numPartitions", "10")
                  .option("lowerBound", "0")
                  .option("upperBound", "1000000"))

    df = reader.load()
    logger.info(f"Extracted {df.count()} records from {platform} {full_table}")
    return df`.trim();
}

function generateWriteFunction() {
  return `
def write_to_target(df, target_platform, target_path, load_method, schema, table,
                    conn_config=None):
    """Write DataFrame to the target platform."""
    conn_config = conn_config or {}
    logger.info(f"Writing {schema}.{table} to {target_platform} at {target_path} ({load_method})")

    output_path = resolve_target_path(target_platform, target_path, schema, table, conn_config)
    write_mode = "overwrite" if load_method in ("overwrite", "full_refresh") else "append"

    if load_method == "upsert":
        logger.warning("Upsert mode: performing overwrite (full replace). "
                       "For true merge, use Delta Lake or database-native upsert.")
        write_mode = "overwrite"

    writer = df.write.mode(write_mode)

    if target_platform in ("adls2", "s3", "local_fs", "nas"):
        writer.option("header", "true").parquet(output_path)
        logger.info(f"Written parquet to {output_path}")
    else:
        writer.parquet(output_path)
        logger.info(f"Written parquet to {output_path}")


def resolve_target_path(target_platform, target_path, schema, table, conn_config):
    """Build the full output path based on target platform."""
    if target_path:
        base = target_path.rstrip("/")
    else:
        base = f"/data/output/{schema}/{table}"

    if target_platform == "adls2":
        account = conn_config.get("account_name", "storageaccount")
        container = conn_config.get("container", "datalake")
        if base.startswith("abfss://"):
            return base
        return f"abfss://{container}@{account}.dfs.core.windows.net/{base.lstrip('/')}"

    if target_platform == "s3":
        bucket = conn_config.get("bucket", "data-bucket")
        if base.startswith("s3a://") or base.startswith("s3://"):
            return base.replace("s3://", "s3a://")
        return f"s3a://{bucket}/{base.lstrip('/')}"

    if target_platform in ("local_fs", "nas"):
        return base

    return base`.trim();
}

function generateColumnMappingFunction() {
  return `
def apply_column_mapping(df, mapping_config):
    """Apply column mapping/renaming from mapping configuration.

    mapping_config: list of dicts with 'source_column' and 'target_column' keys.
    """
    if not mapping_config:
        return df

    mappings = mapping_config if isinstance(mapping_config, list) else mapping_config.get("mappings", [])
    for m in mappings:
        src_col = m.get("source_column") or m.get("source")
        tgt_col = m.get("target_column") or m.get("target")
        if src_col and tgt_col and src_col in df.columns:
            df = df.withColumnRenamed(src_col, tgt_col)
            logger.info(f"Renamed column: {src_col} -> {tgt_col}")
        elif src_col and src_col not in df.columns:
            logger.warning(f"Column {src_col} not found in DataFrame, skipping rename")

    select_cols = [m.get("target_column") or m.get("target") for m in mappings
                   if (m.get("target_column") or m.get("target")) in df.columns]
    if select_cols:
        extra_cols = [c for c in df.columns if c not in select_cols]
        df = df.select(*select_cols, *extra_cols)

    return df`.trim();
}

function generateCleansingFunction() {
  return `
def apply_data_cleansing(df, cleansing_rules):
    """Apply data cleansing transforms to DataFrame columns.

    Supported transforms: trim, lowercase, uppercase, date_iso, remove_nulls,
                          default_value, strip_whitespace.
    """
    if not cleansing_rules:
        return df

    rules = cleansing_rules if isinstance(cleansing_rules, list) else cleansing_rules.get("rules", [])
    for rule in rules:
        col_name = rule.get("column")
        transform = rule.get("transform") or rule.get("type")
        if not col_name or col_name not in df.columns:
            logger.warning(f"Cleansing: column {col_name} not found, skipping")
            continue

        if transform == "trim":
            df = df.withColumn(col_name, F.trim(F.col(col_name)))
        elif transform == "lowercase":
            df = df.withColumn(col_name, F.lower(F.col(col_name)))
        elif transform == "uppercase":
            df = df.withColumn(col_name, F.upper(F.col(col_name)))
        elif transform == "date_iso":
            date_format = rule.get("format", "yyyy-MM-dd")
            df = df.withColumn(col_name, F.date_format(F.to_date(F.col(col_name), date_format), "yyyy-MM-dd"))
        elif transform == "remove_nulls":
            df = df.filter(F.col(col_name).isNotNull())
        elif transform == "default_value":
            default_val = rule.get("value", "")
            df = df.withColumn(col_name, F.when(F.col(col_name).isNull(), F.lit(default_val)).otherwise(F.col(col_name)))
        elif transform == "strip_whitespace":
            df = df.withColumn(col_name, F.regexp_replace(F.col(col_name), r"\\s+", " "))
            df = df.withColumn(col_name, F.trim(F.col(col_name)))
        else:
            logger.warning(f"Unknown cleansing transform: {transform} for column {col_name}")

        logger.info(f"Applied cleansing '{transform}' to column '{col_name}'")

    return df`.trim();
}

function generateDQFunction() {
  return `
def apply_data_quality_checks(df, dq_rules, quarantine_path=None):
    """Run data quality checks and optionally quarantine failed rows.

    Supported checks: not_null, unique, range, regex, min_length, max_length.
    Returns (passed_df, violations_summary).
    """
    if not dq_rules:
        return df, []

    rules = dq_rules if isinstance(dq_rules, list) else dq_rules.get("rules", [])
    violations = []
    quarantine_dfs = []

    for rule in rules:
        col_name = rule.get("column")
        check_type = rule.get("check") or rule.get("type")
        if not col_name or col_name not in df.columns:
            logger.warning(f"DQ: column {col_name} not found, skipping check")
            continue

        failed_df = None

        if check_type == "not_null":
            failed_df = df.filter(F.col(col_name).isNull())
            violation_count = failed_df.count()
            if violation_count > 0:
                violations.append({"column": col_name, "check": "not_null", "failures": violation_count})
                logger.warning(f"DQ not_null: {violation_count} null values in {col_name}")
                df = df.filter(F.col(col_name).isNotNull())

        elif check_type == "unique":
            dup_counts = df.groupBy(col_name).count().filter(F.col("count") > 1)
            dup_count = dup_counts.count()
            if dup_count > 0:
                violations.append({"column": col_name, "check": "unique", "failures": dup_count})
                logger.warning(f"DQ unique: {dup_count} duplicate groups in {col_name}")

        elif check_type == "range":
            min_val = rule.get("min")
            max_val = rule.get("max")
            condition = F.lit(True)
            if min_val is not None:
                condition = condition & (F.col(col_name) >= min_val)
            if max_val is not None:
                condition = condition & (F.col(col_name) <= max_val)
            failed_df = df.filter(~condition)
            violation_count = failed_df.count()
            if violation_count > 0:
                violations.append({"column": col_name, "check": "range", "failures": violation_count,
                                   "min": min_val, "max": max_val})
                logger.warning(f"DQ range: {violation_count} out-of-range values in {col_name}")
                df = df.filter(condition)

        elif check_type == "regex":
            pattern = rule.get("pattern", ".*")
            failed_df = df.filter(~F.col(col_name).rlike(pattern))
            violation_count = failed_df.count()
            if violation_count > 0:
                violations.append({"column": col_name, "check": "regex", "failures": violation_count,
                                   "pattern": pattern})
                logger.warning(f"DQ regex: {violation_count} non-matching values in {col_name}")
                df = df.filter(F.col(col_name).rlike(pattern))

        elif check_type == "min_length":
            min_len = int(rule.get("value", 1))
            failed_df = df.filter(F.length(F.col(col_name)) < min_len)
            violation_count = failed_df.count()
            if violation_count > 0:
                violations.append({"column": col_name, "check": "min_length", "failures": violation_count})
                logger.warning(f"DQ min_length: {violation_count} too-short values in {col_name}")
                df = df.filter(F.length(F.col(col_name)) >= min_len)

        elif check_type == "max_length":
            max_len = int(rule.get("value", 255))
            failed_df = df.filter(F.length(F.col(col_name)) > max_len)
            violation_count = failed_df.count()
            if violation_count > 0:
                violations.append({"column": col_name, "check": "max_length", "failures": violation_count})
                logger.warning(f"DQ max_length: {violation_count} too-long values in {col_name}")
                df = df.filter(F.length(F.col(col_name)) <= max_len)

        else:
            logger.warning(f"Unknown DQ check type: {check_type}")

        if failed_df is not None and quarantine_path:
            quarantine_dfs.append(failed_df)

    if quarantine_dfs and quarantine_path:
        from functools import reduce
        quarantine_all = reduce(lambda a, b: a.unionByName(b, allowMissingColumns=True), quarantine_dfs)
        quarantine_all = quarantine_all.withColumn("_dq_quarantine_ts", F.current_timestamp())
        quarantine_all.write.mode("append").parquet(quarantine_path)
        logger.info(f"Quarantined {quarantine_all.count()} rows to {quarantine_path}")

    if violations:
        logger.info(f"DQ summary: {len(violations)} rule(s) had violations")
    else:
        logger.info("DQ: All checks passed")

    return df, violations`.trim();
}

function generateMaskingFunction() {
  return `
@F.udf(returnType=StringType())
def hash_value(value):
    """Hash a value using SHA-256."""
    if value is None:
        return None
    return hashlib.sha256(str(value).encode("utf-8")).hexdigest()


@F.udf(returnType=StringType())
def partial_mask(value, show_first=2, show_last=2, mask_char="*"):
    """Partially mask a string value, showing first/last N characters."""
    if value is None:
        return None
    s = str(value)
    if len(s) <= show_first + show_last:
        return mask_char * len(s)
    return s[:show_first] + mask_char * (len(s) - show_first - show_last) + s[-show_last:]


def apply_masking(df, masking_rules):
    """Apply security/masking transforms to DataFrame columns.

    Supported types: hash, redact, partial_mask.
    """
    if not masking_rules:
        return df

    rules = masking_rules if isinstance(masking_rules, list) else masking_rules.get("rules", [])
    for rule in rules:
        col_name = rule.get("column")
        mask_type = rule.get("type") or rule.get("mask_type")
        if not col_name or col_name not in df.columns:
            logger.warning(f"Masking: column {col_name} not found, skipping")
            continue

        if mask_type == "hash":
            df = df.withColumn(col_name, hash_value(F.col(col_name).cast("string")))
            logger.info(f"Applied SHA-256 hash to column '{col_name}'")

        elif mask_type == "redact":
            redact_val = rule.get("value", "***REDACTED***")
            df = df.withColumn(col_name, F.lit(redact_val))
            logger.info(f"Redacted column '{col_name}'")

        elif mask_type == "partial_mask":
            show_first = int(rule.get("show_first", 2))
            show_last = int(rule.get("show_last", 2))
            mask_char = rule.get("mask_char", "*")
            df = df.withColumn(
                col_name,
                partial_mask(F.col(col_name).cast("string"),
                             F.lit(show_first), F.lit(show_last), F.lit(mask_char))
            )
            logger.info(f"Applied partial mask to column '{col_name}'")

        else:
            logger.warning(f"Unknown masking type: {mask_type} for column {col_name}")

    return df`.trim();
}

function generateMainFunction(hasColumnMapping, hasCleansing, hasDQ, hasMasking) {
  const lines = [];

  lines.push(`def main():`);
  lines.push(`    parser = argparse.ArgumentParser(description="DataFlow PySpark extraction and transformation")`);
  lines.push(`    parser.add_argument("--source_platform", required=True,`);
  lines.push(`                        choices=["oracle", "sql_server", "postgresql", "mysql", "mongodb"],`);
  lines.push(`                        help="Source database platform")`);
  lines.push(`    parser.add_argument("--target_platform", required=True,`);
  lines.push(`                        choices=["local_fs", "adls2", "s3", "nas"],`);
  lines.push(`                        help="Target storage platform")`);
  lines.push(`    parser.add_argument("--schema", required=True, help="Source schema name")`);
  lines.push(`    parser.add_argument("--table", required=True, help="Source table name")`);
  lines.push(`    parser.add_argument("--load_method", default="append",`);
  lines.push(`                        choices=["append", "overwrite", "full_refresh", "upsert"],`);
  lines.push(`                        help="Load method for target write")`);
  lines.push(`    parser.add_argument("--filter_query", default=None, help="SQL WHERE clause for filtering")`);
  lines.push(`    parser.add_argument("--incremental_column", default=None,`);
  lines.push(`                        help="Column for incremental/partition-based reads")`);
  lines.push(`    parser.add_argument("--target_path", default=None, help="Target output path")`);
  lines.push(`    parser.add_argument("--mapping_file", default=None,`);
  lines.push(`                        help="Path to JSON mapping file for column mapping, cleansing, DQ, masking")`);
  lines.push(`    args = parser.parse_args()`);
  lines.push(``);
  lines.push(`    logger.info(f"Starting extraction: {args.schema}.{args.table} from {args.source_platform} to {args.target_platform}")`);
  lines.push(``);
  lines.push(`    spark = (SparkSession.builder`);
  lines.push(`             .appName(f"dataflow_{args.schema}_{args.table}")`);
  lines.push(`             .getOrCreate())`);
  lines.push(``);
  lines.push(`    try:`);
  lines.push(`        source_config = get_connection_config("SOURCE")`);
  lines.push(`        target_config = get_connection_config("TARGET")`);
  lines.push(``);
  lines.push(`        df = extract_from_source(`);
  lines.push(`            spark, args.source_platform, args.schema, args.table,`);
  lines.push(`            filter_query=args.filter_query,`);
  lines.push(`            incremental_column=args.incremental_column,`);
  lines.push(`            conn_config=source_config,`);
  lines.push(`        )`);
  lines.push(``);
  lines.push(`        mapping_config = None`);
  lines.push(`        if args.mapping_file:`);
  lines.push(`            logger.info(f"Loading mapping file: {args.mapping_file}")`);
  lines.push(`            with open(args.mapping_file, "r") as f:`);
  lines.push(`                mapping_config = json.load(f)`);
  lines.push(``);

  if (hasColumnMapping) {
    lines.push(`        if mapping_config and mapping_config.get("mappings"):`);
    lines.push(`            df = apply_column_mapping(df, mapping_config)`);
    lines.push(``);
  }

  if (hasCleansing) {
    lines.push(`        if mapping_config and mapping_config.get("cleansing"):`);
    lines.push(`            df = apply_data_cleansing(df, mapping_config["cleansing"])`);
    lines.push(``);
  }

  if (hasDQ) {
    lines.push(`        if mapping_config and mapping_config.get("data_quality"):`);
    lines.push(`            quarantine_path = None`);
    lines.push(`            if mapping_config.get("quarantine_path"):`);
    lines.push(`                quarantine_path = mapping_config["quarantine_path"]`);
    lines.push(`            df, dq_violations = apply_data_quality_checks(df, mapping_config["data_quality"], quarantine_path)`);
    lines.push(`            if dq_violations:`);
    lines.push(`                logger.info(f"DQ violations found: {json.dumps(dq_violations)}")`);
    lines.push(``);
  }

  if (hasMasking) {
    lines.push(`        if mapping_config and mapping_config.get("masking"):`);
    lines.push(`            df = apply_masking(df, mapping_config["masking"])`);
    lines.push(``);
  }

  lines.push(`        target_path = args.target_path or f"/data/output/{args.schema}/{args.table}"`);
  lines.push(`        write_to_target(df, args.target_platform, target_path, args.load_method,`);
  lines.push(`                        args.schema, args.table, conn_config=target_config)`);
  lines.push(``);
  lines.push(`        logger.info(f"Pipeline complete: {args.schema}.{args.table}")`);
  lines.push(``);
  lines.push(`    except Exception as e:`);
  lines.push(`        logger.error(f"Pipeline failed: {e}", exc_info=True)`);
  lines.push(`        sys.exit(1)`);
  lines.push(`    finally:`);
  lines.push(`        spark.stop()`);

  return lines.join("\n");
}

