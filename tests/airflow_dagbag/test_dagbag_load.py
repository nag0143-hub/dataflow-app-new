"""
DataFlow DAG YAML → Airflow DagBag Integration Test
====================================================
Tests that dag-factory compatible YAML files generated by DataFlow
can be successfully loaded into Airflow 3.x DagBag.

Tests:
  1. Each YAML file parses and builds DAGs via dag-factory
  2. DAG IDs follow the dataflow__ convention
  3. Tasks are created with correct operator classes
  4. Task dependencies are wired correctly
  5. Default args propagate (owner, retries, etc.)
  6. Schedule is set correctly
  7. Bulk load_yaml_dags works (simulates real Airflow dagbag scan)
  8. Bootstrap generate_dags.py loads all YAMLs
  9. Advanced features YAML with mapping_file references
  10. Realistic pipeline_tasks.py has all expected callables
  11. PySpark script stub has all expected functions
"""

import os
import sys
import traceback
import yaml
import tempfile
import shutil
import importlib.util
import json

os.environ.setdefault("AIRFLOW_HOME", tempfile.mkdtemp())
os.environ["AIRFLOW__CORE__LOAD_EXAMPLES"] = "False"
os.environ["AIRFLOW__DATABASE__SQL_ALCHEMY_CONN"] = "sqlite:////tmp/airflow_test.db"
os.environ["AIRFLOW__CORE__UNIT_TEST_MODE"] = "True"

import airflow
from airflow.models import DagBag
from dagfactory.dagfactory import _DagFactory, load_yaml_dags

TESTS_DIR = os.path.dirname(os.path.abspath(__file__))
STUBS_DIR = os.path.join(TESTS_DIR, "stubs")
MAPPINGS_DIR = os.path.join(STUBS_DIR, "mappings")

YAML_FILES = [
    "flat_file_landing_to_raw.yaml",
    "db_extract_to_dwh.yaml",
    "flat_file_with_transform.yaml",
    "simple_no_sensor.yaml",
    "advanced_features.yaml",
]

PASS = 0
FAIL = 0
ERRORS = []

def check(label, condition, detail=""):
    global PASS, FAIL, ERRORS
    if condition:
        PASS += 1
        print(f"  PASS  {label}")
    else:
        FAIL += 1
        msg = f"  FAIL  {label}" + (f" — {detail}" if detail else "")
        print(msg)
        ERRORS.append(msg)

def test_individual_yaml_files():
    print("\n" + "=" * 60)
    print("TEST 1: Individual YAML file loading via dag-factory")
    print("=" * 60)

    for fname in YAML_FILES:
        fpath = os.path.join(TESTS_DIR, fname)
        print(f"\n--- {fname} ---")

        with open(fpath) as f:
            raw = yaml.safe_load(f)

        dag_ids = [k for k in raw.keys() if not k.startswith("default")]
        check(f"[{fname}] YAML parsed OK", len(dag_ids) > 0, f"dag_ids={dag_ids}")

        try:
            factory = _DagFactory(config_filepath=fpath)
            dags = factory.build_dags()
            check(f"[{fname}] build_dags() succeeded", True)
            check(f"[{fname}] DAG count matches", len(dags) == len(dag_ids),
                  f"expected {len(dag_ids)}, got {len(dags)}")

            for dag_id, dag in dags.items():
                check(f"[{fname}] DAG ID starts with 'dataflow__'",
                      dag_id.startswith("dataflow__"), f"got: {dag_id}")
                check(f"[{fname}] DAG has tasks",
                      len(dag.tasks) > 0, f"task count: {len(dag.tasks)}")

                task_ids = [t.task_id for t in dag.tasks]
                print(f"    Tasks: {task_ids}")

                dag_config = raw[dag_id]

                expected_schedule = dag_config.get("schedule")
                actual_schedule = dag.timetable.serialize() if hasattr(dag, 'timetable') else str(dag.schedule_interval)
                print(f"    Schedule: {expected_schedule} → {actual_schedule}")

                expected_owner = dag_config.get("default_args", {}).get("owner", "airflow")
                for t in dag.tasks:
                    check(f"[{fname}] task '{t.task_id}' owner = '{expected_owner}'",
                          t.owner == expected_owner, f"got: {t.owner}")
                    break

                for task_name, task_config in dag_config.get("tasks", {}).items():
                    deps = task_config.get("dependencies", [])
                    if deps:
                        task = dag.get_task(task_name)
                        upstream_ids = [t.task_id for t in task.upstream_list]
                        check(f"[{fname}] task '{task_name}' upstream deps",
                              set(deps) == set(upstream_ids),
                              f"expected {deps}, got {upstream_ids}")

        except Exception as e:
            check(f"[{fname}] build_dags() succeeded", False, f"Error: {e}")
            traceback.print_exc()


