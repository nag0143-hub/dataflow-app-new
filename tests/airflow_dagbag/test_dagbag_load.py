def test_mapping_json_files():
    print("\n" + "=" * 60)
    print("TEST 10: Mapping JSON files are valid")
    print("=" * 60)

    mapping_files = [f for f in os.listdir(MAPPINGS_DIR) if f.endswith(".json")]
    check("Mapping directory has JSON files", len(mapping_files) >= 2,
          f"got {len(mapping_files)}: {mapping_files}")

    for mf in mapping_files:
        mpath = os.path.join(MAPPINGS_DIR, mf)
        try:
            with open(mpath) as f:
                data = json.load(f)
            check(f"[{mf}] valid JSON", True)
            check(f"[{mf}] has pipeline field", "pipeline" in data)
            check(f"[{mf}] has dataset field", "dataset" in data)
            check(f"[{mf}] has columns array", isinstance(data.get("columns"), list) and len(data["columns"]) > 0)

            for col in data["columns"]:
                check(f"[{mf}] column has source_column",
                      "source_column" in col, f"keys: {list(col.keys())}")
                check(f"[{mf}] column has target_column",
                      "target_column" in col, f"keys: {list(col.keys())}")
                break

        except json.JSONDecodeError as e:
            check(f"[{mf}] valid JSON", False, f"Error: {e}")
        except Exception as e:
            check(f"[{mf}] mapping file check", False, f"Error: {e}")


