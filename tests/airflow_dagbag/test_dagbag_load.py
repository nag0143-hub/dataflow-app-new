def test_bootstrap_generate_dags():
    print("\n" + "=" * 60)
    print("TEST 6: Bootstrap generate_dags.py loading all YAMLs")
    print("=" * 60)

    bootstrap_dir = tempfile.mkdtemp()
    dataflow_subdir = os.path.join(bootstrap_dir, "dataflow")
    os.makedirs(dataflow_subdir, exist_ok=True)

    try:
        for fname in YAML_FILES:
            src = os.path.join(TESTS_DIR, fname)
            dst = os.path.join(dataflow_subdir, fname)
            shutil.copy2(src, dst)

        bootstrap_src = os.path.join(STUBS_DIR, "generate_dags.py")
        bootstrap_dst = os.path.join(bootstrap_dir, "generate_dags.py")
        shutil.copy2(bootstrap_src, bootstrap_dst)

        check("Bootstrap file exists", os.path.isfile(bootstrap_dst))
        check("YAML subdir populated",
              len(os.listdir(dataflow_subdir)) == len(YAML_FILES),
              f"got {len(os.listdir(dataflow_subdir))}")

        spec = importlib.util.spec_from_file_location("generate_dags", bootstrap_dst)
        mod = importlib.util.module_from_spec(spec)

        try:
            spec.loader.exec_module(mod)
            check("Bootstrap module loaded without error", True)
        except Exception as e:
            check("Bootstrap module loaded without error", False, f"Error: {e}")
            traceback.print_exc()

        dag_keys = [k for k in dir(mod) if k.startswith("dataflow__")]
        check("Bootstrap registered DAGs in globals",
              len(dag_keys) >= len(YAML_FILES),
              f"expected >= {len(YAML_FILES)}, got {len(dag_keys)}: {dag_keys}")

        for dk in dag_keys:
            obj = getattr(mod, dk)
            check(f"Bootstrap DAG '{dk}' is valid DAG object",
                  hasattr(obj, 'tasks'), f"type: {type(obj)}")

        print(f"  Bootstrap-registered DAGs: {dag_keys}")

    finally:
        shutil.rmtree(bootstrap_dir, ignore_errors=True)


def test_advanced_features_yaml():
    print("\n" + "=" * 60)
    print("TEST 7: Advanced features YAML with mapping_file references")
    print("=" * 60)

    fpath = os.path.join(TESTS_DIR, "advanced_features.yaml")
    with open(fpath) as f:
        raw = yaml.safe_load(f)

    dag_id = "dataflow__advanced_pipeline"
    check("Advanced YAML has expected DAG ID", dag_id in raw, f"keys: {list(raw.keys())}")

    dag_config = raw[dag_id]
    tasks = dag_config.get("tasks", {})

    check("Advanced DAG has sensor task", "wait_for_source_file" in tasks)
    check("Advanced DAG has spark extract task", "extract_hr__employees" in tasks)
    check("Advanced DAG has ingest task", "ingest_landing__raw_feed" in tasks)

    spark_task = tasks.get("extract_hr__employees", {})
    app_args = spark_task.get("application_args", [])
    check("Spark task has --mapping_file arg", "--mapping_file" in app_args,
          f"args: {app_args}")

    mapping_file_idx = app_args.index("--mapping_file") if "--mapping_file" in app_args else -1
    if mapping_file_idx >= 0 and mapping_file_idx + 1 < len(app_args):
        mapping_path = app_args[mapping_file_idx + 1]
        check("Mapping file path references mappings dir",
              "mappings/" in mapping_path, f"path: {mapping_path}")
        check("Mapping file exists on disk",
              os.path.isfile(mapping_path), f"path: {mapping_path}")

        with open(mapping_path) as f:
            mapping_data = json.load(f)
        check("Mapping JSON has pipeline field",
              "pipeline" in mapping_data, f"keys: {list(mapping_data.keys())}")
        check("Mapping JSON has columns field",
              "columns" in mapping_data and len(mapping_data["columns"]) > 0)
        check("Mapping JSON has cleansing rules",
              "cleansing" in mapping_data and len(mapping_data["cleansing"].get("rules", [])) > 0)
        check("Mapping JSON has data_quality rules",
              "data_quality" in mapping_data and len(mapping_data["data_quality"].get("rules", [])) > 0)
        check("Mapping JSON has masking rules",
              "masking" in mapping_data and len(mapping_data["masking"].get("rules", [])) > 0)

    spark_app = spark_task.get("application", "")
    check("Spark task application points to spark stub",
          "pipeline_spark.py" in spark_app, f"application: {spark_app}")

    check("Spark extract depends on sensor",
          "wait_for_source_file" in spark_task.get("dependencies", []))
    check("Ingest depends on sensor",
          "wait_for_source_file" in tasks.get("ingest_landing__raw_feed", {}).get("dependencies", []))

    try:
        factory = _DagFactory(config_filepath=fpath)
        dags = factory.build_dags()
        check("Advanced features DAG builds successfully", dag_id in dags)

        dag = dags[dag_id]
        check("Advanced DAG has 3 tasks", len(dag.tasks) == 3,
              f"got {len(dag.tasks)}")

        task_ids = sorted([t.task_id for t in dag.tasks])
        print(f"    Advanced DAG tasks: {task_ids}")
    except Exception as e:
        check("Advanced features DAG builds successfully", False, f"Error: {e}")
        traceback.print_exc()


